package jj.org.facade;

public class Bike {

	public void sitOnBike() {
		System.out.println("Sitting on bike now");
	}

	public void removeStand() {
		System.out.println("Stand removed");
	}

	public void checkFuelTank() {
		System.out.println("Fuel tank full, good to go");
	}

	public void startIgnition() {
		System.out.println("Bike key inserted, Ignition on");
	}

	public void startEngine() {
		System.out.println("Engine started");
	}

	public void clutchAndGearUp() {
		System.out.println("Shifted up ");
	}

	public void twistThrottle() {
		System.out.println("Giving full throttle now");
	}

	public void applyBrakes() {
		System.out.println("Brakes applied");
	}

	public void closeThrottle() {
		System.out.println("Throttle closed");
	}

	public void clutchAndGearDown() {
		System.out.println("Shifted down");
	}

	public void stopEngine() {
		System.out.println("Engine stopped");
	}

	public void killIgnition() {
		System.out.println("Ignition off, bike key removed");
	}

	public void putBikeOnStand() {
		System.out.println("Put bike on stand");
	}

	public void getOffBike() {
		System.out.println("Got off bike");
	}

}
